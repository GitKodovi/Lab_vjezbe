    <p >Posebno je  zanimljiv naš posljednje spomenuti &bdquo;sporta&scaron;&ldquo;, moram ga baš tako nazvati jer je  on prvenstveno veslač koji je ove godine čak i kandidirao za olimpijske igre u  Pekingu a koji se još prošle godine odlučio otrčati Zagrebački maraton. Sa  treninzima koji su trajali oko 40 dana istrčao ga je i to za manje od 3 sata  što je inače nedostižna želja većine naših maratonaca. A to je ponovio i ove  godine, 2:57:52. Naravno u trkačem svijetu gotovo nitko ne zna tko je Erik, jer  on nije atletičar, on je veslač a maraton mu je samo malo razbio veslačku  monotoniju.</p>
    <p >U Sinju su ove  godine naši dečki izborili drugo mjesto na ekipnom prvenstvu u polumaratonu (<strong>Tissauer, špani&#263;, Mrkojevi&#263;, Jagati&#263;</strong>).  Ipak bili su veoma blizu Sljemenu koji je pojačan sa mnogo dobrih trkača  pristiglih iz Mladosti i Veterana ove godine imao svoj dan. Sezona je na kraju  ipak bila naša.</p>
    <p >Svemu ovome  treba dodati i to da je A.K.&#381;umberak bio jedini hrvatski klub koji je u  protekle dvije godine nastupio na svim natjecanjima cestovnog kupa bez obzira  gdje su se održavala, te da godišnje sudjeluje u većem ili manjem broju na više  od 50 natjecanja u Hrvatskoj i van nje.</p>
