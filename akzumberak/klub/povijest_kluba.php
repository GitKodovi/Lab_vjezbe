			<h3> POVIJEST KLUBA </h3><br>

	<div class="grid_6 srediIE">

      <p >Atletski klub "Žumberak", osnovan  1997. godine. <br>
        Nesumnjivo je od svog postanka do  danas dao svoj doprinos razvoju hrvatske atletike i cestovnog trčanja, i svakako  doprinio stasanju mnogih naših izvrsnih dugoprugaša i maratonaca.<br />
        S toga klub zaslužuje i ovaj  osvrt na povijest nastanka i razvoja kluba.</p>
      <p class="style1">&nbsp;</p>
      <p class="style1"><strong><u>O postanku kluba</u></strong></p>
      <p class="style1"><img width="424" height="301" src="klub/slike/povijest kluba_clip_image002_0000.jpg" align="left" hspace="12" /><br /><br />
	  Iako danas u Hrvatskoj  postoji preko 40 klubova koji su registrirani i natječu se u hrvatskoj  cestovnoj lizi, u vrijeme našeg nastanka nije bilo puno klubova  specijaliziranih prvenstveno za cestovno trčanje i bavili se, kako se to danas  zove, van stadionskom atletikom. S toga A.K.&#381;umberak možemo smatrati jednim od  pionira omasovljavanja cestovnog trčanja kod nas, naravno uz dužno poštovanje  najstarijem klubu ovakve vrste kod nas &bdquo;Veteranu&ldquo;. </p>
      <p >No vratimo se malo i ispred  vremena registracije kluba. <br />
        Godine 1995. u Rimu se održavaju  Prve Svjetske Vojne igre na kojima nastupa jedan dio naših atletičara koji su  tada bili članovi hrvatske vojske, a i dio njih koji su samo za ove svrhe bili  mobilizirani. U svakom slučaju maratonsku ekipu tamo činili su naši tada  izvrsni maratonci Paripovi&#263;, Gavranovi&#263;, Lončarević i Smičiklas. Treba  spomenuti i nastupe na 10000 m  našeg &#268;ernića i na 5000 m  španića i Kovaća Siniše. Namjerno sam spomenuo baš ovu skupinu jer su svi oni u  odre&#273;eno vrijeme bili članovi ovog kluba.</p>
      <p >1996. u organizaciji D.  Badovinca, Žamarije, M Radića i drugih entuzijasta sa Žumberka odlučila je  organizirati &bdquo;1.&#381;umberački maraton&ldquo; od sela Radatovića do Sošica na stazi od 15,6 km. Nastupilo je tada 28  dugoprugaša a pobjede su odnijeli Robert Dragan, Konjik i Jambrec. Bio je to  značajan doga&#273;aj za Sošice najveće selo žumberačke općine koje broji manje od  200 stanovnika, a i za ovaj kraj koji je koju godinu kasnije postao i Zagrebu  najbližim parkom prirode, nakon Medvednice. </p>
      <p >Već tada na prijedlog g.  Badovinca okuplja se članstvo A.K.&#381;umberka a me&#273;u prvim članovima bili su  klupski trener &#268;ernić i Danijela, Lončarevi&#263;, Rakipovi&#263;, Iža Marijan i Mirela,  Dunja Kravos, Zmazek, Konjik, Radić P. Stanić i Smičiklas. Za predsjednika  kluba izabire se g. Janko Radi&#263;, tadašnji direktor Farmacije.</p>
      <p class="style1"><strong><u>&nbsp;</u></strong></p>
      <p class="style1"><strong><u>Prve godine </u></strong></p>
      <p class="style1">1997. i 1998. prvi puta  se na nekoliko uličnih trka, kojih tada nije bilo previše, pojavljuje skupina  gore navedenih trkača koji nastupaju pod imenom Žumberka. Tada se na natjecanja  išlo ležerno i prvenstvena želja je bila uz dobro otrčanu utrku sresti svoje  prijatelje i poznanike iz drugih gradova koje je dijelila zajednička ljubav  Atletika. Jedino tada vidljivo natjecanje bilo je u tome tko će bolje počastiti  i svoje i članove drugih klubova na utrkama koja organiziraju ili putovanjima na  koja su išla. Dovoljno je za primjer navesti nastup na Bečkom maratonu 1996.  kada je g. Molnar tada predsjednik Croatia Maratona &ndash; Poljana organizirao  autobus za svojih 10-tak članova a onda je naravno ostatak mjesta besplatno  podijelio svim ostalim trkačima koji su tada imali želju otputovati u Be&#269;, i  naravno tamo u jednom od ljepših Bečkih restorana počastio sve goste sa obilnom  večerom.</p>
      <p >1998. godine  osniva se i CRO-CUP naš prvi kup koji je uveo i natjecanje izme&#273;u klubova u  kome su bila obuhvaćena cestovna trčanja. Tu prvu godinu završavamo sa 3.  mjestom u muškoj kategoriji i 1. mjestom u kategoriji atletičarki. Uz Veteran,  Poljanu i Varaždinsku banku u sjevernoj regiji pojavio se još jedan izvrstan  klub Žumberak. Od tada pa sve do danas ženska ekipa Žumberka zadržala je prvo  mjesto u ovom kupu. Atletičari sve ostale godine osim 2001 tako&#273;er završavaju  godinu na pobjedničkom postolju.<br />
        Klub je  nastavio sa tradicijom održavanja Žumberačke utrke a na krajnjem istoku zemlje  organizira polumaraton u mjestu&nbsp; Batina,  kojeg je provodio nekoliko godina.</p>
      <p class="style1"><strong><u>1999. &ndash; 2002. godine dominacije  Atletičarki </u></strong><br />
        Ovo su zaista bile godine  dominacije naših atletičarki ali prvo ćemo spomenuti i naše trkače. <br />
        1999. u Zagrebu se održavaju 2.  Svjetske vojne igre i opet se dio naših atletičara priprema za nastup. Veliki  dio godine provodimo na pripremama u Makarskoj. Na pojedinačnom prvenstvu  države u maratonu koji se te godine održao na otoku Ugljanu drugo mjesto iza  Prešnjaka <img width="588" height="394" src="klub/slike/povijest kluba_clip_image002.jpg" align="left" hspace="12" /><img width="275" height="28" src="klub/slike/povijest kluba_clip_image003.gif" align="left" hspace="12" alt="Text Box: Dunja, Marija i Danijela na trci u Batini" />zauzima  Lončarević sa 2:41:00. &#268;olić tako&#273;er osvaja drugo mjesto na Zagrebačkom  maratonu sa 2:42:04, 2. Smičiklas nastupa na vojnom prvenstvu u maratonu u  švicarskom Bielu i sa 2:49:95 najbolje je plasirani član ekipe. <br />
        Legendarna je utrka dvoje  maratonaca koji su tako&#273;er u Bielu nastupali u vojnoj patroli na 100km naših  časnika Zmazeka i Rakipovića. Oni su u četvrtom nastupu hrvatske patrole  napokon i uspjeli završiti utrku zajedničkim ulaskom u cilj kako je propisano i  sa vremenom 9 sati i 48 minuta postavili hrvatski vojni rekord koji je još i  danas važeći. <br />
        Te godine dio naših trkača  sudjeluje i na Utrci Nacionalnih Parkova trčeći štafetno od Kopačkog rita do  Mljeta u dužini od 1500 km. Izvrsni su bili nastupi naših članova i  idućih godina. Svakako su značajni i uspjesi Stanića, &#268;ernića, Iže i drugih<br />
        Ipak bila je to godina  atletičarki, a pojava dviju mladih djevojaka 30-godišnje Dunje Kravos i  23-godišnje Marije Trošić svakako je obilježio godinu. Bilo ih je posvuda. I na  našim cestovnim trkama i nastupu u sklopu maratona na 2.Svj. Vojnim Igrama a  doprinos je Dunjin i pratnja Zmazeka na biciklu u utrci na 100km. No uz njih  bio je tu i cijeli niz kvalitetnih atletičarki kao što su Patricija Glu&scaron;&#269;ić i  Mirjana Kovači&#263;, Iže Mirele, Danijele &#268;erni&#263;, Ružice Paradiš i dr.<br />
        O njihovom uspjehu najbolje  govori tablica 10 najuspješnijih maratonaca u 1999. me&#273;u kojima je čak 5 naših  članica. Iduću godinu je članicom Žumberka postala i Marija Paulus.</p>
