      <p >Drago nam je  što su tako upravo u našim redovima stasali i budući izvrsni maratonci poput <strong>Hrvoja Kovaća</strong> koji je nastupio na svom  prvom maratonu u Zagrebu 2003. godine sa vremenom 2:58:35 a samo godinu kasnije  na prvenstvu države na Zagrebačkom maratonu otrčao  svoj još uvijek najbolji maraton za 2;34:02.  Iako dvije godine kasnije napušta naš klub ponosni smo da je svoj početak i  najbolje rezultate postigao upravo kod nas. <strong>Hendrik Bottger</strong> van konkurencije te 2004. trči Zagrebački maraton  za 2:31.</p>
