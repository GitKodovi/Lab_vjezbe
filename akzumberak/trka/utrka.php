	<h3 align="left">6 km</h3></br>
	<div style="float:right; margin:10px;"><a href="trka/slike/slap2.jpg" ><img src="trka/slike/slap2.jpg" width="200px"></a> </div>
	<p align="left">
		Prošle godine prvi put je organizirana i utrka na 6 km. Vjerujemo da možemo reči jedna od najljepših kračih staza koje možete otrčati u Hrvatskoj.  </br>
		Start je u Sošicama, u isto vrijeme kada i starta glavna trka u Radatovićima. Trči se prema selu Sopote, selu koje je prije desetak godina dobilo nagradu
		za najljepše selo u Zagrebačkoj županiji. Iako stanlo boravište u selu ima samo jedna stannovnica, mnoge stare kuće su obnovljene na način da se nije 
		mjenjala tradicionalna, za Žumberak karakteristična arhitektura,</br>
		Iza sela nastavljamo cestom koja vodi uz kanjon kroz koji se spušta potok kupćina nedaleko od svog izvora, i na kraju puta od 2,5 km stižemo do znamenitog
		Sopotskog slapa. Ne toliko poznatog koliko zaslužuje njegova ljepota. Tu se okrečemo i vračamo istim putem natrag.</br>
		Iako ne sasvim ravna ova je staza ipak mnogo lakša, bez jačih uspona i padova i smatramo ugodna za trčanje i po svojoj konfiguraciji i 
		ljepoti koja je okružuje. 
	</p>
	 
