	<p align="left">
		Uz akciju koju provodimo u suradnji sa Hrvatskim Olipmpijskim Odborom "Edukativne razglednice o fair play-u" ove godine svi sudionici popunjavaju razglednice 
		koje se šalju Hrvatskom fair play odboru...</br>
		Tradicija održavanja tječjih trka traje od prvog našeg natjecanja. Svrha je potaknuti i one najmlađe na sudjelovanje. One koji su tek prohodali pa do završetka
		osnovne škole. </br>
		Najmlađi predškolci trče 100 m, dok oni najstariji 1 km. Sve se odvija u parkiću u centru Sošica. Veselje ne samo za one koji sudjeluju, već i za roditelje i 
		navijače koji bez problema mogu imati pogled na cijeli tok natjecanja. </br>
		Da bi ugođaj bio potpun organizator se brine da svi koji uđu u cilj dobiju neki od poklončića naših sponzora te paketa Cedevite. Najbolji po kategorijama naravno
		i medalje.
