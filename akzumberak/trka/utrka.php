	<h3 align="left">KAKO DO NAS</h3></br> 
	<div style="float:right; margin:10px;"><a href="trka/slike/pribic.jpg" ><img src="trka/slike/pribic.jpg" width="200px"></a> </div>
	<p align="left"> Oni koji se odluče doći na našu utrku moći će odabrati osobni automobil ili jedan od besplatnih autobusa koje će čekati iza Ciboning tornja u Zagrebu. </p>
	<br>
	
	<p align="left">
		Iza Jastrebarskog skrenut će prema Krašiću i proći "Dolinom kardinala“, mjestu u kojem se rodio i završio život kardinal Stepinac. Kilometar dalje 
		Pribić, rodno mjesto kardinala Kuharića. Barokni dvorac grkokatoličkih biskupa, s kapelicom sv. Marije podigao je grof Petar Zrinski.<br>
		Iza tih vrata Žumberka počinjemo se uzdizati bregovitim i sve bujnijim krajolikom i uskoro nas dočeka tabla s obavijesti da smo ušli u Park prirode 
		Žumberak. Premda slabo posjećen, uglavnom od strane planinara i biciklističkih entuzijasta ovo je nakon Medvednice Zagrebu najbliži park prorode.
	</p>
	<br>
