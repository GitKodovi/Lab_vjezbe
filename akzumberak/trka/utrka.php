		Srest ćemo mnoga usamljena sela, prepuna starih kuća. Proći ćemo kroz Kostanjevac, selo u kojem je sjedište općine Žumberak, koja danas broji oko 
		40-tak sela i manje od 900-tinjak stanovnika.</br>
		Od Kostanjevca do Sošica preostalo je još oko 12 km. Važno je obratiti pažnju na jedino križanje koje na ovoj cestici postoji, a nalazi se 500-tinjak metara 
		iza Kostanjevca. Postoji lijevi odvojak preko malog mostiča. Tamo treba skrenuti. Ne produžiti ravno. Postoji tabla, ali za svaki slučaj. 
