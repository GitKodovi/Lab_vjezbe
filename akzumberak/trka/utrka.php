	<h3 align="left">15 km</h3><br>
	<div style="float:left; margin:10px;"><a href="utrka/slike/jana.jpg" ><img src="trka/slike/jana.jpg" width="300px"></a> </div>
	<p align="left">
		Utrka od 15 km je naša tradicionalna utrka i trči se od sela Radatovići do Sošica. Utrka koja se boduje za Hrvatsku cestovnu ligu i kup. </br>
		Radatovići su najzapadnije Žumberačko selo uz granicu sa Slovenijom. Sošice su najveće i najnaseljenije selo Žumberka u kome živi manje od 100 stanovnika.
		Mnogi koji su trčali ovu stazu reči će da utrka nije laka i da je prate manji ili veći usponi i nizbrdice, ali i mogučnost da se kroz tih 15 km osjete ljepote
		Parka prirode Žumberak. </br>

		Iako se trči asfaltiranom cestom cijelo vrijeme biti ćete okruženi netaknutom, čistom prirodom a okruženje kroz koje ćete prolaziti 
		izmjenjivati će svoje prizore.</br>
		  
		Dodali bi i to da je ova utrka jedna od prvih u jesenjem djelu sezone pa čak i onima manje pripremljenima, uz pravilan tempo trčanje, dati će potreban tonus
		mišićima da se lakše nose sa svim predstojećim natjecanjima.
	</p>
	
